import 'package:flutter/material.dart';

/// Color for AppTheme theme extension
class AppColors {
  AppColors._();
  static Color get primaryLight => blue;
  static Color get primaryDark => amber;

  static const Color black = Colors.black;
  static const Color white = Colors.white;
  static const Color blue = Color(0xFF42A7DE);
  static const Color amber = Color(0xFFFF8C00);
}
